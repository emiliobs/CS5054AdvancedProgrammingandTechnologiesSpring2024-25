    private void btnConvertActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnConvertActionPerformed
    {//GEN-HEADEREND:event_btnConvertActionPerformed

        try
        {
            double amount = Double.parseDouble(txtInput.getText());
            double rate = Double.parseDouble(cbRates.getSelectedItem().toString());
            double result;

            if (rbUSDtoGBP.isSelected())
            {
                result = amount * rate;
            }
            else
            {
                result = amount / rate;
            }
            txtResult.setText(String.format("%.2f", result));
        }
        catch (NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null,"Invalid input. Please enter a numeric value", "Error", JOptionPane.ERROR_MESSAGE);
            txtInput.setText("");
            txtResult.setText("");
        }


    }//GEN-LAST:event_btnConvertActionPerformed

